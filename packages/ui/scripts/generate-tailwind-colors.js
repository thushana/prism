/**
 * Generate Tailwind palette CSS ($prefix --color-tailwind-{family}-{shade})
 * and tailwind-color-values.ts from `tailwindcss/colors`.
 *
 * Must match PRISM_TAILWIND_COLOR_LOOP order in styles/prism-color.ts.
 */

const fs = require("fs");
const path = require("path");

const tailwindColors = require("tailwindcss/colors");

/** Same order as PRISM_TAILWIND_COLOR_LOOP in prism-color.ts */
const TW_LOOP_ORDER = [
  "slate",
  "gray",
  "zinc",
  "neutral",
  "stone",
  "red",
  "orange",
  "amber",
  "yellow",
  "lime",
  "green",
  "emerald",
  "teal",
  "cyan",
  "sky",
  "blue",
  "indigo",
  "violet",
  "purple",
  "fuchsia",
  "pink",
  "rose",
];

const SHADE_KEYS = [
  "50",
  "100",
  "200",
  "300",
  "400",
  "500",
  "600",
  "700",
  "800",
  "900",
  "950",
];

function generateCSS() {
  let css = `/**
 * Tailwind palette — prefixed theme variables
 *
 * Auto-generated from tailwindcss/colors (Tailwind v4 default colors).
 * Use with PrismColor when palette is "tailwind":
 *   var(--color-tailwind-{family}-{shade})
 *
 * Import in globals:
 *   @import "./tailwind-colors.css" layer(theme);
 */

@theme {
`;

  for (const family of TW_LOOP_ORDER) {
    const shades = tailwindColors[family];
    if (!shades || typeof shades !== "object") {
      console.warn(`[generate-tailwind-colors] skip missing family: ${family}`);
      continue;
    }
    css += `\n  /* ${family} */\n`;
    for (const shadeKey of SHADE_KEYS) {
      const val = shades[shadeKey];
      if (val === undefined) continue;
      css += `  --color-tailwind-${family}-${shadeKey}: ${val};\n`;
    }
  }

  css += "}\n";
  return css;
}

function generateTS() {
  const numericUnion =
    "50 | 100 | 200 | 300 | 400 | 500 | 600 | 700 | 800 | 900 | 950";

  let ts = `/**
 * Tailwind palette - resolved color strings (often oklch(...) from Tailwind v4).
 *
 * Auto-generated by scripts/generate-tailwind-colors.js from tailwindcss/colors.
 * DO NOT EDIT MANUALLY.
 */

export type TailwindPaletteFamily =
  ${TW_LOOP_ORDER.map((f) => `"${f}"`).join(" | ")};

export const tailwindColorValues: Record<
  TailwindPaletteFamily,
  Record<${numericUnion}, string>
> = {
`;

  for (const family of TW_LOOP_ORDER) {
    const shades = tailwindColors[family];
    if (!shades || typeof shades !== "object") {
      throw new Error(`Missing tailwind color family: ${family}`);
    }
    ts += `  ${family}: {\n`;
    for (const shadeKey of SHADE_KEYS) {
      const val = shades[shadeKey];
      if (val === undefined) {
        throw new Error(`Missing shade ${family}-${shadeKey}`);
      }
      ts += `    ${shadeKey}: ${JSON.stringify(val)},\n`;
    }
    ts += `  },\n`;
  }

  ts += `} as const;\n`;
  return ts;
}

const stylesDir = path.join(__dirname, "../styles");

const cssPath = path.join(stylesDir, "tailwind-colors.css");
fs.writeFileSync(cssPath, generateCSS(), "utf-8");
console.log(`✅ Generated ${cssPath}`);

const tsPath = path.join(stylesDir, "tailwind-color-values.ts");
fs.writeFileSync(tsPath, generateTS(), "utf-8");
console.log(`✅ Generated ${tsPath}`);
